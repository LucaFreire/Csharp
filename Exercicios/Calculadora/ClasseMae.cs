public class Calculadora
{
    public double Soma(double num1, double num2)
    {
        return num1 + num2;
    }
    public double Sub(double num1, double num2)
    {
        return num1 - num2;
    }
    public double Mult(double num1, double num2)
    {
        return num1 * num2;
    }
    public double Div(double num1, double num2)
    {
        return num1 / num2;
    }
}
